// Demo program illustrating how to write 9 bit three times in a 16 bit WOM (R_fixed=1.6875).
// First two writes use a direct coding map, last write uses a parity check matrix 
// The code can be considered as a <19511,512>/16 2-write code with "coset coding", for which a subset
// of the 1st write vectors was found that internally contains a <512,512> code.
// Matrix manipulation uses bit parallel operations to increase speed
// Comments to bert.o.dobbelaere[at]telenet[dot]be

#include <stdio.h>
#include <assert.h>
#include <stdlib.h>
#include <string.h>

#define NTESTS 20 // Number of test rounds to perform (arbitrary)

#define N 16 // Total nr of pattern bits
#define K 7 // Max bits set in 2nd write pattern
#define NSYMBOLS (1<<(N-K))
#define NCOMB (1<<N)

typedef unsigned u32;


u32 write1_2_map[NCOMB]; // Decoding map for 1st and 2nd writes
bool has_last_write_solution[NCOMB]; // Indicate patterns eligible for 1st and 2nd writes
u32 firstwrite_table[NSYMBOLS]; // Encoding map for write 1
u32 secondwrite_table[NSYMBOLS][NSYMBOLS]; // Encoding map for write 2
u32 pattern_to_fixedbits[NCOMB]; // Pads a 1st/2nd write pattern to exactly 7 bits, so we can work with a square matrix

void init_write1_2_map();
u32 getNextInitSymbol();


// Parity check matrix (16x9 bit) for last write encode/decode cycle
const u32 a[N]={0x137,0x36,0x1EE,0xC1,0xB6,0x13,0x156,0x6B,0x39,0xFF,0x135,0xC7,0x123,0xDC,0x29,0x98};


// Handles binary matrices with max 32 columns, nrows>=ncolumns
// 1 row per input word. Input matrix is not copied, so modified!
bool hasfullrank(u32 a[], u32 nrows, u32 ncols)
{
	u32 i=0;
	for(u32 diagbit=1ULL<<(ncols-1) ; diagbit!=0 ; diagbit>>=1)
	{
		for(u32 k=i+1;k<nrows;k++)
		{
			if(a[i]&diagbit)
				break;
			a[i]^=a[k];
		}
		if((a[i]&diagbit)==0)
			return false; //no pivot found
		
		for(u32 k=i+1;k<nrows;k++)
			if(a[k]&diagbit)
				a[k]^=a[i];
		i++;
	}
	return true;
}


// Recursively called routine, finding full rank minors of the parity check matrix
u32 countinv(const u32 a[], u32 rows, u32 lowestremoved, u32 pattern)
{
	u32 count=0;
	u32 m[rows];
	
	memcpy(m,a,rows*sizeof(u32));
	if(hasfullrank(m,rows,N-K))
	{
		has_last_write_solution[pattern]=true;
		count++;
		write1_2_map[pattern]=getNextInitSymbol();
		if(rows > (N-K))
		{
			for(u32 r=0;r<lowestremoved;r++)
			{
				u32 idx=0;
				for(u32 i=0;i<r;i++)
					m[idx++]=a[i];
				for(u32 i=r+1;i<rows;i++)
					m[idx++]=a[i];
				count+=countinv(m,rows-1,r, pattern|(1<<r));
			}
		}	
	}	
	return count;
}

// Count number of bits set in a pattern
u32 bitcount(u32 p)
{
	u32 count=0;
	while(p)
	{
		count+=p&1;
		p>>=1;
	}
	return count;
}

// Computes syndrome using the parity check matrix
u32 compute_syndrome(u32 pattern)
{
	u32 syndrome=0;
	for(u32 n=0;n<N;n++,pattern>>=1)
		if(pattern&1)
			syndrome^=a[n];
	return syndrome;
}


// Iterate over patterns while all bits from mask remain set.
u32 next_pattern(u32 p, u32 mask) 
{
	u32 i=1;
	p++;
	while((p&mask)!=mask)
	{
		if((~p&mask)&i)
			p+=i;
		i*=2;
	}	
	return p;
}


// One time initialisation of memory structures. May be replaced by hardcoded tables.
void init_tables()
{
	memset(has_last_write_solution,0,sizeof(has_last_write_solution));
	memset(write1_2_map,0,sizeof(write1_2_map));
	
	printf("Initializing tables.\n");

	u32 nvectors=countinv(a,N,N,0);

	printf("Available 1st/2nd write vectors = %u\n",nvectors);
	
	bool covered1[NSYMBOLS];
	memset(covered1,0,sizeof(covered1));
	
	u32 countfirst=0;
	
	for(u32 p=0;p<NCOMB;p++)
		if(has_last_write_solution[p] && !covered1[write1_2_map[p]])
		{
			bool covered2[NSYMBOLS];
			u32 patterns2[NSYMBOLS];
			memset(covered2,0,sizeof(covered2));
			
			for(u32 q=p;q<NCOMB;q=next_pattern(q,p))
				if(has_last_write_solution[q])
				{
					patterns2[write1_2_map[q]]=q;
					covered2[write1_2_map[q]]=true;
				}
			
			bool ok=true;
			for(u32 n=0;n<NSYMBOLS;n++)
				if(!covered2[n])
				{
					ok=false;
					break;
				}
			if(ok)
			{
				covered1[write1_2_map[p]]=true;
				firstwrite_table[write1_2_map[p]]=p;
				memcpy(secondwrite_table[write1_2_map[p]], patterns2, sizeof(patterns2));
				countfirst++;
			}	
		}
	
	assert(countfirst==NSYMBOLS);
	
	memset(pattern_to_fixedbits,0,sizeof(pattern_to_fixedbits));
	
	for(u32 p=0;p<NCOMB;p++)
		if(has_last_write_solution[p])
		{
			for(u32 q=p;q<NCOMB;q=next_pattern(q,p))
			{
				if(has_last_write_solution[q] && (bitcount(q) == K))
				{
					pattern_to_fixedbits[p]=q;
					break;
				}
				
				assert(q<NCOMB); // padding to 7 bits must be found
			}
		}
}

// Decode the 1st or 2nd write symbol from a pattern
u32 decode1_2(u32 pattern)
{
	return write1_2_map[pattern];
}

// Encode the 1st write symbol into a pattern
u32 encode1(u32 s1)
{
	return firstwrite_table[s1];
}

// Encode the 2nd write symbol into a pattern, knowing the pattern after 1st write
u32 encode2(u32 s2, u32 pattern1)
{
	u32 s1=decode1_2(pattern1);
	return secondwrite_table[s1][s2];
}

// Decode the last write symbol from a pattern
u32 decode3(u32 pattern)
{
	return compute_syndrome(pattern);
}

// Solve linear system over GF(2)
// Gauss-Jordan elimination of enhanced matrix, rows are bit-packed.
void solvesystem(u32 A[]) 
{
	u32 nrows=N-K; // ncolumns = nrows+1
	u32 i=0;
	for(u32 diagbit=1 ; diagbit<(1u<<nrows) ; diagbit<<=1)
	{
		for(u32 k=i+1;k<nrows;k++)
		{
			if(A[i]&diagbit)
				break;
			A[i]^=A[k];
		}
		assert((A[i]&diagbit)!=0); // pivot must be found, otherwise not invertible
		
		for(u32 k=i+1;k<nrows;k++)
			if(A[k]&diagbit)
			{
				A[k]^=A[i];
			}
		i++;
	}
	
	i=0;
	for(u32 diagbit=1 ; diagbit<(1u<<nrows) ; diagbit<<=1)
	{	
		for(u32 k=0;k<i;k++)
			if(A[k]&diagbit)
			{
				A[k]^=A[i];
			}
		i++;
	}
	
	// A is now unity matrix enhanced with solution column (bit position N-K)
}

// Encodes the 3rd write symbol in a pattern, knowing the pattern after 2nd write
u32 encode3(u32 s3, u32 pattern2)
{
	u32 fixedbits=pattern_to_fixedbits[pattern2]; // exactly 7 bits, superset of pattern2 bits
	
	u32 target = s3 ^ decode3(pattern2);
	
	u32 m[N-K]={0};
	u32 rows=0;
	for(u32 b=0;b<N;b++)
	{
		if(((fixedbits>>b)&1)==0)
		{
			for(u32 i=0;i<(N-K);i++) // Add coefficient column
				m[i] |=  ((a[b]>>i)&1) << rows;
			rows++;
		}
	}
	
	for(u32 i=0;i<(N-K);i++) // Add enhanced column
		m[i] |= ((target>>i)&1) << rows;

	solvesystem(m);
	
	u32 pattern=pattern2;
	
	rows=0;
	for(u32 b=0;b<N;b++)
	{
		if(((fixedbits>>b)&1)==0)
		{
			pattern |= ((m[rows]>>(N-K))&1)<<b;
			rows++;
		}
	}
	return pattern;
}


int main()
{
	printf("\nThis example illustrates the encoding/decoding of a <(2^9),(2^9),(2^9)>/16  WOM code.\n");
	printf("Assumption is that encoder and decoder know the write number.\n");
	init_tables();
	
	// Test the encoding/decoding process
	printf("Testing our code by encoding/decoding random 9 bit values (hex):\n\n");
	printf("Encode 1       Pattern 1      Decode 1      |   Encode 2          Pattern 2      Decode 2      |   Encode 3          Pattern 3      Decode 3\n\n");
	
	for(u32 n=0;n<NTESTS;n++)
	{
		// Pick 3 symbols to be encoded
		u32 s1_enc= rand()%NSYMBOLS;
		u32 s2_enc= rand()%NSYMBOLS;
		u32 s3_enc= rand()%NSYMBOLS;
	
		// Encode/decode 1st symbol
		u32 pattern1=encode1(s1_enc);
		
		printf("e1(%03X)   =>   p1=%04X   ",s1_enc,pattern1);
		
		if(pattern1 >= NCOMB)
		{
			printf("\nERROR: 1st pattern invalid.\n");
			assert(0);
		}
		
		u32 s1_dec = decode1_2(pattern1);
		printf("=>   d1(p1)=%03X    |  ",s1_dec);
	
		if(s1_dec!=s1_enc)
		{
			printf("ERROR: 1st symbol decode failed !\n");
			assert(0);
		}
	
		// Encode/decode 2nd symbol
		u32 pattern2=encode2(s2_enc,pattern1);
		
		printf(" e2(%03X,p1)   =>   p2=%04X",s2_enc,pattern2);
		
		if((pattern2 >= NCOMB) || ((pattern2&pattern1)!=pattern1) /* Check against "cheating" bits */)
		{
			printf("ERROR: 2nd pattern invalid.\n");
			assert(0);
		}

		u32 s2_dec = decode1_2(pattern2);
		printf("   =>   d2(p2)=%03X    |  ",s2_dec);
		if(s2_dec!=s2_enc)
		{
			printf("ERROR: 2nd symbol decode failed ! (Expected %03X, decoded %03X)\n",s2_enc, s2_dec);
			assert(0);
		}

		// Encode/decode 3rd symbol
		u32 pattern3=encode3(s3_enc,pattern2);
		
		printf(" e3(%03X,p2)   =>   p3=%04X",s3_enc,pattern3);
		
		if((pattern3 >= NCOMB) || ((pattern3&pattern2)!=pattern2) /* Check against "cheating" bits */)
		{
			printf("ERROR: 3rd pattern invalid.\n");
			assert(0);
		}
	
		u32 s3_dec = decode3(pattern3);
		printf("   =>   d3(p3)=%03X\n",s3_dec);
		if(s3_dec!=s3_enc)
		{
			printf("ERROR: 3rd symbol decode failed ! (Expected %03X, decoded %03X)\n",s3_enc, s3_dec);
			assert(0);
		}
	}
	
}

